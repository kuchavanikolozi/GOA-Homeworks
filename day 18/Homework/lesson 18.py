# We are presented with a list indexed ['$', '%', '^','V'].
# We need to extract the middle two members and then print them. There are two ways to remove it
# 1. Conventional indices
# 2. Negative (-) indices

symbols =  ['$', '%', '^','V']
print(symbols[1:3])
print(symbols[1])
print(symbols[2])
print(symbols[-3])
print(symbols[-2])